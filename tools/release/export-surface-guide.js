#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';
import { createCli } from '../../src/shared/cli.js';
import { resolveToolRoot } from '../shared/dict-utils.js';
import { loadShippedSurfaces } from './surfaces.js';

const ROOT = resolveToolRoot();
const DEFAULT_OUTPUT = path.join('docs', 'guides', 'release-surfaces.md');

const parseArgs = () => createCli({
  scriptName: 'pairofcleats release-surfaces',
  options: {
    out: { type: 'string', default: DEFAULT_OUTPUT }
  }
})
  .strictOptions()
  .parse();

const renderSurface = (surface) => {
  const releaseCheckStepsByPhase = new Map();
  for (const step of surface.releaseCheck.steps) {
    const key = String(step.phase || 'smoke');
    const bucket = releaseCheckStepsByPhase.get(key) || [];
    bucket.push(step);
    releaseCheckStepsByPhase.set(key, bucket);
  }
  const lines = [
    `## ${surface.name}`,
    '',
    `- id: \`${surface.id}\``,
    `- owner: \`${surface.owner}\``,
    `- support: \`${surface.supportLevel}\``,
    `- packaging boundary: \`${surface.packagingBoundary}\``,
    `- publish boundary: \`${surface.publishBoundary}\``,
    `- version source: \`${surface.versionSource}\``,
    `- runtime targets: ${surface.runtimeTargets.map((value) => `\`${value}\``).join(', ') || '_none_'}`,
    `- platforms: ${surface.platforms.map((value) => `\`${value}\``).join(', ') || '_none_'}`,
    '',
    `Build contract: \`${surface.build.kind}\``,
    ''
  ];
  for (const sourcePath of surface.build.sourcePaths) {
    lines.push(`- build source: \`${sourcePath}\``);
  }
  for (const output of surface.build.outputs) {
    lines.push(`- build output: \`${output}\``);
  }
  if (surface.build.sourcePaths.length === 0 && surface.build.outputs.length === 0) {
    lines.push('- build paths: _none declared_');
  }
  lines.push('');
  lines.push(`Install contract: \`${surface.install.kind}\``);
  lines.push(`- ${surface.install.summary}`);
  lines.push('');
  lines.push('Smoke contract:');
  lines.push(`- ${surface.smoke.summary}`);
  if (surface.releaseCheck.steps.length > 0) {
    lines.push('- automated release-check steps by phase:');
    for (const phase of ['build', 'install', 'boot', 'smoke']) {
      const steps = releaseCheckStepsByPhase.get(phase) || [];
      if (steps.length === 0) continue;
      lines.push(`  - ${phase}:`);
      for (const step of steps) {
        lines.push(`    - \`${step.id}\` -- ${step.label}`);
      }
    }
  } else {
    lines.push('- automated release-check steps: _not yet enabled_');
  }
  lines.push('');
  return lines.join('\n');
};

const main = () => {
  const argv = parseArgs();
  const outPath = path.resolve(ROOT, argv.out || DEFAULT_OUTPUT);
  const registry = loadShippedSurfaces(ROOT);
  const content = [
    '# Release Surfaces',
    '',
    'This file is generated by `node tools/release/export-surface-guide.js`.',
    '',
    `Registry: \`${path.relative(ROOT, registry.registryPath).replace(/\\/g, '/')}\``,
    '',
    'The release pipeline, artifact verification, and smoke contracts should consume the same surface registry described below.',
    '',
    ...registry.surfaces.map((surface) => renderSurface(surface))
  ].join('\n');
  fs.mkdirSync(path.dirname(outPath), { recursive: true });
  fs.writeFileSync(outPath, `${content.trimEnd()}\n`);
  console.log(`wrote ${path.relative(ROOT, outPath).replace(/\\/g, '/')}`);
};

main();
